    private void guardar() {
        EntradaSalida.Guardar g = new Guardar();
        DimensionMia dim = new DimensionMia(util.getWidth(), util.getHeight());

        String nombre;
        nombre = JOptionPane.showInputDialog(this, "PONGA EL NOMBRE DE ARCHIVO");
        if (nombre == null) {
            JOptionPane.showMessageDialog(this, "AVISO: ACCION CANCELADA");
            return;
        } else if (nombre.equals("")) {
            JOptionPane.showMessageDialog(this, "AVISO: NOMBRE NO VALIDO");
            return;
        }
        nombre=nombre+".rmr";

        int aux;
        File fl;
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        aux = chooser.showOpenDialog(this);
        if (JFileChooser.APPROVE_OPTION == aux) {
            fl = chooser.getSelectedFile();
            if (g.guardar(control.getObjetos(), dim, fl.getPath(), nombre)) {
                JOptionPane.showMessageDialog(this, "AVISO SE PUDO GUARDAR EXITOSAMENTE");
            } else JOptionPane.showMessageDialog(this, "ERROR: NO SE PUDO GUARDAR");
        } else {
            JOptionPane.showMessageDialog(this, "AVISO: ACCION CANCELADA");
        }
    }

