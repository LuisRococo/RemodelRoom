    private void cargar (File fl){
        if (fl==null) {
            JOptionPane.showMessageDialog(null, "AVISO: EL ARCHIVO QUE SELECCIONO ES NULO");
            return;
        }
        Cargar cargar=new Cargar(fl);
        if (cargar.cargar()){
            JOptionPane.showMessageDialog(null, "AVISO: SE PUDIERON CARGAR TODOS LOS COMPONENTES");
            DimensionMia dim=cargar.getDim();
            Vent_Principal vent=new Vent_Principal(dim.getWidth(), dim.getHeight(),cargar.getObjetos());
        } else {
            JOptionPane.showMessageDialog(null, "ERROR: NO SE PUDO CARGAR");
        }
    }

