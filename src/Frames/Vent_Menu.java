    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int aux;
        File fl;
        FileNameExtensionFilter pngFilter = new FileNameExtensionFilter("ARCHIVO PARA REMODEL ROOM", "rmr");
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(pngFilter);
        aux = chooser.showOpenDialog(this);
        if (JFileChooser.APPROVE_OPTION == aux) {
            fl = chooser.getSelectedFile();
            cargar(fl);
        } else {
            JOptionPane.showMessageDialog(null, "AVISO: ACCION CANCELADA");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


